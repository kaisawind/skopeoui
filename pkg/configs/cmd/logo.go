func ArtLogo(name string) string {
	var out []string
	for _, v := range name {
		lines, ok := artFont[strings.ToUpper(string(v))]
		if !ok {
			continue
		}
		if len(out) == 0 {
			out = append([]string{}, lines...)
		} else {
			for i := range out {
				if len(lines) > i {
					out[i] += lines[i]
				}
			}
		}
	}
	return "\n" + strings.Join(out, "\n") + "\n\n"
}
